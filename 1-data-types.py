# Python is dynamically typed, thats means we dont need to define type for variable while declaring

# string datatype
name="John Doe"
print(type(name))
print(name)

# int datatype
age=27
print(type(age))
print(age)

# float datatype
height=5.9
print(type(height))
print(height)

# bool datatype
isEligible = False
print(type(isEligible))
print(isEligible)

# NoneType datatype
noValue=None
print(type(noValue))
print(noValue)

# input method Reads a string from standard input.
num1 = input("Enter you first number ")
num2 = input("Enter you second number ")
print(int(num1)+int(num2))
